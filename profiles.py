def _norm_cell(s: str) -> str:
    return re.sub(r"[^a-z0-9 ]+", " ", (s or "").lower())


def _spec(v):
    """Normalize a field spec to (positive_keywords, negative_keywords)."""
    if isinstance(v, dict):
        return v.get("kw", []), v.get("not", [])
    return v, []


def _cell_match(kw: str, cell: str, cell_ns: str) -> bool:
    # Match against the cell and a space-stripped variant, so words that
    # pdfplumber splits ("indicato r", "descriptio n") still match ("indicator").
    return kw in cell or kw.replace(" ", "") in cell_ns


def match_profile(header: list[str]) -> dict | None:
    """Best-matching profile for a header row, with the column map, score, the
    missing expected columns, and — crucially — coreCoverage (fraction of the
    table's *essential* columns that were found). coreCoverage is the honest
    confidence signal: a narrow-but-complete table scores 1.0 even if it lacks
    optional columns."""
    norm = [_norm_cell(c) for c in header]
    norm_ns = [c.replace(" ", "") for c in norm]
    best = None
    for prof in TABLE_PROFILES:
        mapping: dict[str, int] = {}
        used: set[int] = set()
        for field, spec in prof["expectedColumns"].items():
            pos, neg = _spec(spec)
            for i, cell in enumerate(norm):
                if i in used:
                    continue
                if any(_cell_match(k, cell, norm_ns[i]) for k in pos) and \
                   not any(_cell_match(k, cell, norm_ns[i]) for k in neg):
                    mapping[field] = i
                    used.add(i)
                    break
        if not all(r in mapping for r in prof["requiredFields"]):
            continue
        score = len(mapping)
        if score < prof["minFields"]:
            continue
        core = prof.get("coreFields", [])
        core_matched = sum(1 for f in core if f in mapping)
        core_cov = round(core_matched / len(core), 2) if core else 1.0
        # Rank by (core coverage, score) so the semantically-best profile wins.
        rank = (core_cov, score)
        if best is None or rank > best["_rank"]:
            best = {
                "type": prof["type"],
                "collection": prof["collection"],
                "mapping": mapping,
                "score": score,
                "expectedCount": len(prof["expectedColumns"]),
                "matchedColumns": list(mapping.keys()),
                "missingExpected": [f for f in prof["expectedColumns"] if f not in mapping],
                "coreFields": core,
                "coreCoverage": core_cov,
                "_rank": rank,
            }
    if best:
        best.pop("_rank", None)
    return best


_CERT_CELL_RE = re.compile(r"^#?\s*[AC]\d{2,5}$")
_STD_REF_RE = re.compile(r"\b(?:fips|sp)\s*-?\s*\d{2,4}", re.IGNORECASE)
_ALGO_TOKENS = ("aes", "sha", "hmac", "rsa", "ecdsa", "dsa", "drbg", "kbkdf",
                "kdf", "kas", "kts", "ecdh", "cmac", "gcm", "gmac", "xts", "ctr",
                "tdes", "triple-des", "ml-kem", "ml-dsa", "slh-dsa", "hash", "kmac")


def _looks_algo(cell: str) -> bool:
    low = cell.lower()
    return bool(_STD_REF_RE.search(cell)) or any(t in low for t in _ALGO_TOKENS)


def content_profile(rows: list[list[str]]) -> dict | None:
    """Recognize an algorithm-detail table by its CONTENT when no header matched.

    Many Security Policies present the algorithm table with a detached or missing
    header (the PDF parser drops it), leaving an obviously-tabular body: a column
    of ACVP cert labels (#C1925 / A3548) beside a column of algorithm names with
    FIPS/SP references. We detect those columns directly and synthesize an
    ``approvedAlgorithm`` mapping so the rows still type cleanly.
    """
    body = [r for r in rows if any((c or "").strip() for c in r)]
    if len(body) < 2:
        return None
    ncols = max(len(r) for r in body)
    cols = [[(r[i] if i < len(r) else "").strip() for r in body] for i in range(ncols)]

    def frac(col, pred):
        nz = [c for c in col if c]
        return sum(1 for c in nz if pred(c)) / len(nz) if nz else 0.0

    cert_idx = next((i for i, c in enumerate(cols)
                     if frac(c, lambda x: bool(_CERT_CELL_RE.match(x))) >= 0.5), None)
    name_scores = sorted(((frac(c, _looks_algo), i) for i, c in enumerate(cols) if i != cert_idx),
                         reverse=True)
    if not name_scores or name_scores[0][0] < 0.5:
        return None
    name_idx = name_scores[0][1]
    mapping = {"name": name_idx}
    if cert_idx is not None:
        mapping["cavpCert"] = cert_idx
    for i, c in enumerate(cols):  # label the remaining columns by content
        if i in mapping.values():
            continue
        blob = " ".join(c).lower()
        if any(w in blob for w in ("bit", "key size", "strength", "curve", "modulus")):
            mapping.setdefault("keySize", i)
        elif any(w in blob for w in ("encrypt", "decrypt", "sign", "verif", "generation",
                                     "authentication", "derivation", "agreement", "wrap")):
            mapping.setdefault("useFunction", i)
        elif any(w in blob for w in ("counter", "feedback", "mode", "cbc", "ecb", "ofb")):
            mapping.setdefault("modeMethod", i)
    if len(mapping) < 2:
        return None
    return {"type": "approvedAlgorithm", "collection": "approvedAlgorithmsDetailed",
            "mapping": mapping, "score": len(mapping), "expectedCount": 8,
            "matchedColumns": list(mapping), "missingExpected": [],
            "coreFields": ["name"], "coreCoverage": 1.0, "byContent": True}


def map_row(match: dict, header: list[str], row: list[str], source: dict) -> dict:
    """Map a data row to canonical fields per the matched profile. Unmapped but
    non-empty cells are retained under `extraColumns` so nothing is dropped."""
    width = len(header)
    cells = (row + [""] * width)[:width]
    mapping = match["mapping"]
    obj: dict = {}
    for field, idx in mapping.items():
        v = cells[idx].strip()
        if v:
            obj[field] = v
    if not obj.get("name"):
        obj["name"] = next((c for c in cells if c.strip()), "")
    mapped_idx = set(mapping.values())
    extra = {}
    for i, c in enumerate(cells):
        if i not in mapped_idx and c.strip():
            key = (header[i] or f"col{i}").strip() or f"col{i}"
            extra[key] = c.strip()
    if extra:
        obj["extraColumns"] = extra
    obj["source"] = source
    return obj
