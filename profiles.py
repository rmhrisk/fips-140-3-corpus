"""Canonical table profiles — cross-document structure constraints.

FIPS 140-3 Security Policies, though authored independently, share a small set of
mandated tables with predictable columns (SSP/CSP tables, services tables, ports
tables, algorithm tables, self-tests, ...). We declare those shared shapes once,
then use them to (a) *recognize* a table regardless of vendor wording, (b) *map*
its columns to canonical field names so the data is queryable, and (c) *validate*
the extraction by reporting which expected columns were found vs. missing.

Each profile field lists the header keyword(s) that identify it. A field spec is
either a list of positive keywords, or a dict ``{"kw": [...], "not": [...]}`` —
the ``not`` list are negative keywords that *prevent* a match, which stops
adjacent similar columns from cross-mapping (e.g. keeping "Strength" from being
claimed by the "Security Function" column).

``coreFields`` are the columns a genuine instance of the table must have; they
drive the honest confidence metric (see match_profile → coreCoverage) so that a
table which legitimately has few columns is not scored as low-confidence.
"""
